    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
      
          
       if (radioEmpleMensual.isSelected()) {
            String path = "I:\\orestes_2\\src\\reportes\\report_sueldo_mensual_empleado.jasper";
            JasperReport jr = null;
            try {
                Map parametro = new HashMap();
                parametro.put("mes", txtMes.getText());
                
                jr = (JasperReport) JRLoader.loadObjectFromFile(path);
                JasperPrint jp = JasperFillManager.fillReport(jr, parametro, cn);
                JasperViewer jw = new JasperViewer(jp, false);

                jw.setVisible(true);

            } catch (JRException ex) {
                Logger.getLogger(reportEMPLE.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (radioXempleMensual.isSelected()) {
            String path = "I:\\orestes_2\\src\\reportes\\report_sueldo_op.jasper";
            JasperReport jr = null;
            try {
                Map parametro = new HashMap();
                parametro.put("id", txtIdop.getText());
                parametro.put("mes", txtMes.getText());
                jr = (JasperReport) JRLoader.loadObjectFromFile(path);
                JasperPrint jp = JasperFillManager.fillReport(jr, parametro, cn);
                JasperViewer jw = new JasperViewer(jp, false);

                jw.setVisible(true);

            } catch (JRException ex) {
                Logger.getLogger(reportEMPLE.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        if (radioEmpleCateMensual.isSelected()) {
            String path = "I:\\orestes_2\\src\\reportes\\report_sueldo_mensual.jasper";
            JasperReport jr = null;
            try {
                Map parametro = new HashMap();
                parametro.put("mes", txtMes.getText());
                parametro.put("year", txtYear.getText());
                jr = (JasperReport) JRLoader.loadObjectFromFile(path);
                JasperPrint jp = JasperFillManager.fillReport(jr, parametro, cn);
                JasperViewer jw = new JasperViewer(jp, false);

                jw.setVisible(true);

            } catch (JRException ex) {
                Logger.getLogger(reportEMPLE.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
         
    }//GEN-LAST:event_btnMostrarActionPerformed

