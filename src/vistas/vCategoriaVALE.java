    public void agregarCATE(){
   
        
        
        dCategoriaVALE dts=new dCategoriaVALE();
        
        dts.setCategoria(txtCateOP.getText());
        
        func.save(dts);
   
        txtCateOP.setText("");
        mostrar();
        
   
   }
   
   public void mostrar(){
    
         DefaultTableModel model;

        List<dCategoriaVALE> lista = func.getAll();
        String[] titulos = {"CATEGORIA"};
        model = new DefaultTableModel(null, titulos);
        
        Object data[] = new Object[2];
        for (dCategoriaVALE lista1 : lista) {
            data[0] = lista1.getCategoria();
            
            
            model.addRow(data);
        }
        tableCategoria.setModel(model);
    
    
    }
    
