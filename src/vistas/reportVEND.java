    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
      
          
       if (radioComiMensual.isSelected()) {
            String path = "I:\\orestes_2\\src\\reportes\\comisiones_vendedores.jasper";
            JasperReport jr = null;
            try {
                Map parametro = new HashMap();
                parametro.put("mes", txtMes.getText());
                parametro.put("year", txtYear.getText());
                jr = (JasperReport) JRLoader.loadObjectFromFile(path);
                JasperPrint jp = JasperFillManager.fillReport(jr, parametro, cn);
                JasperViewer jw = new JasperViewer(jp, false);

                jw.setVisible(true);

            } catch (JRException ex) {
                Logger.getLogger(reportVEND.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (radioComiVendM.isSelected()) {
            String path = "I:\\orestes_2\\src\\reportes\\comisiones_vend_detalle.jasper";
            JasperReport jr = null;
            try {
                Map parametro = new HashMap();
                 parametro.put("ID", txtIdop.getText());
                  parametro.put("MES", txtMes.getText());
                parametro.put("YEAR", txtYear.getText());
                jr = (JasperReport) JRLoader.loadObjectFromFile(path);
                JasperPrint jp = JasperFillManager.fillReport(jr, parametro, cn);
                JasperViewer jw = new JasperViewer(jp, false);

                jw.setVisible(true);

            } catch (JRException ex) {
                Logger.getLogger(reportVEND.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
      
        
    }//GEN-LAST:event_btnMostrarActionPerformed

