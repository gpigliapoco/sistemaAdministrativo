    private void tableTransporteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTransporteMouseClicked
        if (evt.getClickCount() == 1) {
            int fila = tableTransporte.getSelectedRow();

            String cod = tableTransporte.getValueAt(fila, 0).toString();
           
            GenericRepository<dTransporte,Long> func=new GenericRepository<dTransporte, Long>(factory) {
        };
            
            long cod1 = Long.valueOf(cod);
            dts1 = func.getById(cod1);

            txtId.setText(dts1.getIdTrasnporte().toString());
            txtNombre.setText(dts1.getNombre());
            txtDireccion.setText(dts1.getDireccion());
            txtTelefono.setText(dts1.getTelefono());
            txtCiudad.setText(dts1.getCiudad());
            
            habilitarTabla();
            btnGuardar.setEnabled(false);


        }
    }//GEN-LAST:event_tableTransporteMouseClicked

