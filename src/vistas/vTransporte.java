    public void guardar(){
        
         if (txtNombre.getText().length()==0) {
            JOptionPane.showMessageDialog(null,"FALTA TRANSPORTE","ADVERTENCIA",JOptionPane.ERROR_MESSAGE);
            txtNombre.requestFocus();
            
            return ;
            
        }
         
          if (txtDireccion.getText().length()==0) {
            JOptionPane.showMessageDialog(null,"FALTA DIRECCION","ADVERTENCIA",JOptionPane.ERROR_MESSAGE);
            txtDireccion.requestFocus();
            
            return ;
            
        }
    
       GenericRepository<dTransporte,Long> func=new GenericRepository<dTransporte, Long>(factory) {
        };
        
        dTransporte dts=new dTransporte();
        
        dts.setNombre(txtNombre.getText());
        dts.setDireccion(txtDireccion.getText());
        dts.setCiudad(txtCiudad.getText());
        dts.setTelefono(txtTelefono.getText());
        func.save(dts);
        JOptionPane.showMessageDialog(rootPane, "TRANSPORTE GUARDADO");
        mostrar();
    
    }
    
    public void editar(){
        
        GenericRepository<dTransporte,Long> func=new GenericRepository<dTransporte, Long>(factory) {
        };
    
        dts1.setNombre(txtNombre.getText());
        dts1.setDireccion(txtDireccion.getText());
        dts1.setCiudad(txtCiudad.getText());
        dts1.setTelefono(txtTelefono.getText());
        func.update(dts1);
        JOptionPane.showMessageDialog(rootPane, "TRANSPORTE EDITADO");
        mostrar();
        habilitar();
    
    
    }
    
    public void mostrar(){
        
        GenericRepository<dTransporte,Long> func=new GenericRepository<dTransporte, Long>(factory) {
        };
    
         DefaultTableModel model;

        List<dTransporte> lista = func.getAll();
        String[] titulos = {"ID", "TRANSPORTE"};
        model = new DefaultTableModel(null, titulos);
        
        Object data[] = new Object[2];
        for (dTransporte lista1 : lista) {
            data[0] = lista1.getIdTrasnporte();
            data[1] = lista1.getNombre();
            
            model.addRow(data);
        }
        tableTransporte.setModel(model);
        tableTransporte.getColumnModel().getColumn(0).setMaxWidth(50);
    
    
    }
    
    public void eliminar(){
        
        GenericRepository<dTransporte,Long> func=new GenericRepository<dTransporte, Long>(factory) {
        };
    
        func.delete(dts1);
       mostrar();
    
    
    }

